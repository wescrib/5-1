public class classFunctions {

	public static void main(String[] args) {
	System.out.println("Absolute: " + Math.abs(-90.8));
	System.out.println("Max: " + Math.max(43243.5, 53253.4));
	System.out.println("Min: " + Math.min(43243.5, 53253.4));
	System.out.println("Round: " + Math.round(45.432));
	System.out.println("Exp: " + Math.pow(9, 4));
	System.out.println("Radical: " + Math.sqrt(25));
	System.out.println("Random: " + Math.random());
	
	
	}

}
